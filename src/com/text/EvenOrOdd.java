package com.text;

public class EvenOrOdd {

	public static void main(String[] args)
	{
      int A[] = {2,3,4,5,6,7,8};
      for(int i=0;i<A.length;i++)
      {
    	 if(A[i]%2==0)
    	 {
    		 System.out.println(A[i]+"is Even no");
    	 }
    	 else
    	 {
    		 System.out.println(A[i]+"is odd no");

    	 }
    	 
      }
	}

}
